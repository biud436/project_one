package core.board.notice;

public class BoardBean {
	
	private	int ctxtno;
	private String wrtnm;
	private String ctitle;
	private String ctxt;
	private int pos;
	private String cpwd;
	private int reply;
	private int viewcnt;
	private String wrtdate;
	private String filename;
	
	
	public int getCtxtno() {
		return ctxtno;
	}
	public void setCtxtno(int ctxtno) {
		this.ctxtno = ctxtno;
	}
	public String getWrtnm() {
		return wrtnm;
	}
	public void setWrtnm(String wrtnm) {
		this.wrtnm = wrtnm;
	}
	public String getCtitle() {
		return ctitle;
	}
	public void setCtitle(String ctitle) {
		this.ctitle = ctitle;
	}
	public String getCtxt() {
		return ctxt;
	}
	public void setCtxt(String ctxt) {
		this.ctxt = ctxt;
	}
	public int getPos() {
		return pos;
	}
	public void setPos(int pos) {
		this.pos = pos;
	}
	public String getCpwd() {
		return cpwd;
	}
	public void setCpwd(String cpwd) {
		this.cpwd = cpwd;
	}
	public int getReply() {
		return reply;
	}
	public void setReply(int reply) {
		this.reply = reply;
	}
	public int getViewcnt() {
		return viewcnt;
	}
	public void setViewcnt(int viewcnt) {
		this.viewcnt = viewcnt;
	}
	public String getWrtdate() {
		return wrtdate;
	}
	public void setWrtdate(String wrtdate) {
		this.wrtdate = wrtdate;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	
}
